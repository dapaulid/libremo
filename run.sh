#!/bin/bash -e

BUILDDIR="build"

if [ ! -d "$BUILDDIR" ]; then
	echo -e "\e[1;36m================================================================================\e[0m"
	echo -e "\e[1;36m                             P R E B U I L D                                    \e[0m"
	echo -e "\e[1;36m================================================================================\e[0m"
 	mkdir $BUILDDIR
	cd $BUILDDIR
	cmake ..
else
	cd $BUILDDIR
fi

echo -e "\e[1;36m================================================================================\e[0m"
echo -e "\e[1;36m                                B U I L D                                       \e[0m"
echo -e "\e[1;36m================================================================================\e[0m"
make

echo -e "\e[1;36m================================================================================\e[0m"
echo -e "\e[1;36m                            U N I T   T E S T                                   \e[0m"
echo -e "\e[1;36m================================================================================\e[0m"
bin/unit_tests

echo -e "\e[1;36m================================================================================\e[0m"
echo -e "\e[1;36m                              C O V E R A G E                                   \e[0m"
echo -e "\e[1;36m================================================================================\e[0m"
lcov --capture --directory . --quiet --output-file coverage.info
lcov --summary coverage.info

echo -e "\e[1;36m================================================================================\e[0m"
echo -e "\e[1;36m                              E X A M P L E                                     \e[0m"
echo -e "\e[1;36m================================================================================\e[0m"
bin/example
echo -e "\e[1;36m================================================================================\e[0m"